% Data generation for tests on cummax

% *************************************************************************
% Test 1
% *************************************************************************
% cumulative minumum for real values along a row vector
v1 = [8 9 1 10 6 1 3 6 10 10];
M1 = cummax(v1);


% *************************************************************************
% Test 2
% *************************************************************************
% cumulative minumum for real values along a column vector
v2 = v1';
M2 = cummax(v2,'reverse');


% *************************************************************************
% Test 3
% *************************************************************************
% Along the second dimension of the 3-dimensional vector
v3 = randi([0,50],5,8,4);
M3 = cummax(v3);


% *************************************************************************
% Test 4
% *************************************************************************
% Complex input
mag = randi([1,3],1,10);
phase = randn(1,10);

v4 = mag.*exp(2*pi*1i*phase);

disp(abs(v4));
disp(angle(v4));
M4 = cummax(v4);


% *************************************************************************
% Test 5
% *************************************************************************
% Complex input
mag = 1;
temp = randn(1,1);

phase = [temp, -temp, -temp, temp];

v5 = mag.*exp(2*pi*1i*phase);

disp(abs(v5));
disp(angle(v5));
M5 = cummax(v5);

save('test.mat','v1','v2','v3','v4','v5','M1','M2','M3','M4','M5');

