% Generating data for testing lpc function

% *************************************************************************
% Test 1: Single data as column vector
% *************************************************************************
noise = randn(50000,1);
x = filter(1,[1,1/2,1/3,1/4],noise);
x1 = x(45904:50000);

p1 = 3;
[a1,g1] = lpc(x1,p1);


% *************************************************************************
% Test 2: Single data as row vector
% *************************************************************************
x2 = x1';
p2 = 2;
[a2,g2] = lpc(x2,p2);


% *************************************************************************
% Test 3: Multiple data columns
% *************************************************************************
x3 = [x1, x(40904:45000)];
p3 = 3;
[a3,g3] = lpc(x3,p3);


% *************************************************************************
% Test 4: Complex input
% *************************************************************************
noise = randn(50000,1) + randn(50000,1)*1i;
x = filter(1,[1,1/6,1/2,1/4,1/5],noise);
x4 = x(45904:50000);

p4 = 4;
[a4,g4] = lpc(x4,p4);

% Saving:
save('test.mat','x1','p1','a1','g1','x2','p2','a2','g2','x3','p3',...
    'a3','g3','x4','p4','a4','g4');

