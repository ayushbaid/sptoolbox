% test data generation for fftfilt

% *****************************************************************************
% Test 1: Single input col vector and single filter
% *****************************************************************************
x1 = sin(1:2000);
b1 = [1 1/2];

y1 = fftfilt(b1,x1);


% *****************************************************************************
% Test 2: Single input row vector and single filter
% *****************************************************************************
y2 = fftfilt(b1,x1');


% *****************************************************************************
% Test 3: Input matrix and single filter
% *****************************************************************************
x3 = [sin(1:500)', log(1:500)'];
b3 = [1 1/4 1/8];
y3 = fftfilt(b3,x3);


% *****************************************************************************
% Test 4: Single input vector and multiple filters
% *****************************************************************************
b4 = [1, 1/5; 1/4, 1/8];
y4 = fftfilt(b4,x1);


% *****************************************************************************
% Test 5: Input matrix and filter matrix
% *****************************************************************************
y5 = fftfilt(b4,x3);


save('test_data.mat','x1','b1','y1','y2','x3','b3','y3','b4','y4','y5');
